package com.woi.user.domain.enums;

/**
 * Context enum - Defines the context/filter
 * Determines which wheel/navigator is active
 * Note: Content Context is always SUCCESS (Wheel of Islam)
 */
public enum Context {
    SUCCESS,    // Success context (Wheel of Islam - categories)
    LIFE,       // Life context (Wheel of Life)
    BUSINESS,   // Business context (Wheel of Business)
    WORK        // Work context (Wheel of Work)
}
